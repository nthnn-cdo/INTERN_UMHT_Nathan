// Operator Perbandingan
// Operator perbandingan digunakan untuk membandingkan dua buah nilai
// Operator perbandingan terdiri dari:
// == : sama dengan
// === : identik (sama dengan nilai dan tipe data)
// != : tidak sama dengan
// <> : tidak sama dengan
// !== : tidak identik
// < : kurang dari
// > : lebih dari
// <= : kurang dari sama dengan
// >= : lebih dari sama dengan

// $a == $b;
// $a === $b;
// $a != $b;
// $a !== $b;
// $a < $b;
// $a > $b;
// $a <= $b;
// $a >= $b;

var_dump ("10" == 10);
var_dump ("10" === 10);

?>