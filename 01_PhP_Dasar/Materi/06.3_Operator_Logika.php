<?php
// Operator Logika
// Operator logika digunakan untuk menggabungkan pernyataan-pernyataan kondisi
// Operator logika terdiri dari:
// && : dan
// || : atau
// ! : tidak

// $a && $b;
// $a || $b;
// ! $a;

var_dump(true && true); // true
var_dump(true && false); // false
var_dump(true || false); // true
var_dump(!true) // false
?>
